# We can use a LIST to store a sequence of numbers (or other stuff)
# Example
list1 = [1, 2, 3, 4, 5]
print(list1)

# We can iterate through a list using a for loop:
for i in list1:
    print(i)

# We can get the length of a list using the len() function:
print(len(list1))

# We can add an element to the end of a list using the append() method:
list1.append(6)

# We can get the element at a specific index using square brackets:
print(list1[0])
print(list1[1])
print(list1[2])


